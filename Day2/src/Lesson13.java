public class Lesson13 {
    public static void main(String[] args) {
        int a = 3, b = 4, c = 3;

        System.out.println(a + " = " + b + " true or false? -- " + (a == b));
        System.out.println(a + " = " + c + " true or false? -- " + (a == c));
        System.out.println(a + " != " + b + " true or false? -- " + (a != b));
        System.out.println(a + " != " + c + " true or false? -- " + (a != c));
        System.out.println(a + " < " + b + " true or false? -- " + (a < b));
        System.out.println(a + " > " + b + " true or false? -- " + (a > b));
        System.out.println(a + " >= " + b + " true or false? -- " + (a >= b));
        System.out.println(a + " <= " + c + " true or false? -- " + (a <= c));
    }
}
