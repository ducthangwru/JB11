public class Lesson7 {
    public static void main(String[] args) {
        int a = 10, b = 3, c = 2;
        System.out.println(a + " + " + b + " = "+ (a+b)); //phép cộng 2 số
        System.out.println(a + " - " + b + " = "+ (a-b)); //phép trừ 2 số
        System.out.println(a + " * " + b + " = "+ (a*b)); //phép nhân 2 số
        System.out.println(a + " / " + b + " = "+ (a/b)); //phép chia 2 số
        System.out.println(a + " % " + b + " = "+ (a%b)); //phép chia lấy dư 2 số
    }
}
